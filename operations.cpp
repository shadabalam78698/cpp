// Operaters In c++
#include<iostream>

using namespace std;

int main(){
    int a=4, b=5;
    cout<<"operations in c++:"<<endl;
    cout<<"following are the type of operations in c++"<<endl;
    cout<<"the value of a + b is "<<a + b<<endl;
    cout<<"the value of a / b " <<a/b<<endl;
    cout<<"the value of a ++ is " <<a++<<endl;
    cout<<a;
    cout<<endl;
    // assigment operator
    // comparision operator
    cout<<"following are the types of comparision operators in c++"<<endl;
    cout<<"the value of a == b is "<<(a==b)<<endl;
    cout<<"the value of a <= b is "<<(a<=b)<<endl;
    cout<<"the value of a >= b is "<<(a>=b)<<endl;
    cout<<"the value of a > b is "<<(a>b)<<endl;
    cout<<"following are the logical operator"<<endl;
    cout<<"the value of this logical and operator ((a==b) && (a<b)) is "<<((a==b) && (a<b))<<endl;
    cout<<"the value of this logical or operator ((a==b) || (a<b)) is "<<((a==b) || (a<b))<<endl;
    cout<<"the value of this "<<(!(a==b))<<endl;


    return 0;
}